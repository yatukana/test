package com.example.demo.model;

public class Event {

	String guid;
	
	String sample;
	
	String uid;

	public String getGuid() {
		return guid;
	}

	public String getUid() {
		return uid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getSample() {
		return sample;
	}

	public void setSample(String sample) {
		this.sample = sample;
	}

}
